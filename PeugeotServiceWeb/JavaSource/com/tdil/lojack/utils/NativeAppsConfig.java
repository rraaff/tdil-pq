package com.tdil.lojack.utils;

public class NativeAppsConfig {

	private long cameraNativeRefreshTime;
	private String videoCarNative;
	private String videoPetsNative;
	private String preventNativeUrl;
	private String petsNativeUrl;
	
	public long getCameraNativeRefreshTime() {
		return cameraNativeRefreshTime;
	}
	public void setCameraNativeRefreshTime(long cameraNativeRefreshTime) {
		this.cameraNativeRefreshTime = cameraNativeRefreshTime;
	}
	public String getVideoCarNative() {
		return videoCarNative;
	}
	public void setVideoCarNative(String videoCarNative) {
		this.videoCarNative = videoCarNative;
	}
	public String getVideoPetsNative() {
		return videoPetsNative;
	}
	public void setVideoPetsNative(String videoPetsNative) {
		this.videoPetsNative = videoPetsNative;
	}
	public String getPreventNativeUrl() {
		return preventNativeUrl;
	}
	public void setPreventNativeUrl(String preventNativeUrl) {
		this.preventNativeUrl = preventNativeUrl;
	}
	public String getPetsNativeUrl() {
		return petsNativeUrl;
	}
	public void setPetsNativeUrl(String petsNativeUrl) {
		this.petsNativeUrl = petsNativeUrl;
	}
}
