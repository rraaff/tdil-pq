package com.tdil.ljpeugeot.feeds.model;

public class ModelImportRecord {

	private String name;
	private String description;
	private String monthwarranty;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getMonthwarranty() {
		return monthwarranty;
	}
	public void setMonthwarranty(String monthwarranty) {
		this.monthwarranty = monthwarranty;
	}
	
}
